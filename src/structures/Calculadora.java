package structures;

public class Calculadora {
    public int somar(int a, int b){
        int result = 0;
        result += a;
        result += b;

        return result;
    }

    public int somar(int a, int b, int c){
        int result = 0;
        result += a;
        result += b;
        result += c;

        return result;
    }
}
